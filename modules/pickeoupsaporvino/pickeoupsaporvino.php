    public function hookDisplayPickeoUpsAporvinoNotification($params){
        
        global $cart;
        
        $sql = 'SELECT * FROM '._DB_PREFIX_.'address WHERE id_address = '.(int)$cart->id_address_delivery.' and deleted = 0  ' ;
        $address =  Db::getInstance()->getRow($sql);

        $sql = 'SELECT * FROM ups_postal_code WHERE postal_code = '. $address['postcode'] ;
        $reference_address =  Db::getInstance()->getRow($sql);
        
        $peso = 0;
        $sobrecargo_total = 0;
        
        foreach($cart->getProducts() as $p){
            $product = new Product($p['id_product']);
            foreach ($product->getCarriers() as $c) {
                $name = strtolower($c['name']);
                if(strpos($name, 'aporvino') !== false){
                    $peso += (float)$product->weight;
                    break;
                }
            }
        }
        
        if($reference_address){
            if($reference_address['remote'] == 1){
                if(Configuration::get("PICKEO_UPS_valor_minimo_remota") > $peso){
                    $sobrecargo_area = Configuration::get("PICKEO_UPS_sobrecargo_remota");
                    $sobrecargo_total = $peso * $sobrecargo_area;
                }
            }
            else {
                if(Configuration::get("PICKEO_UPS_valor_minimo_no_metropolitana") > $peso){
                    $sobrecargo_area = Configuration::get("PICKEO_UPS_sobrecargo_no_metropolitana");
                    $sobrecargo_total = $peso * $sobrecargo_area;
                }
            }
        }
        
        $this->context->smarty->assign('mostrar_sobrecargo', $sobrecargo_total > 0 ? true : false);
        $this->context->smarty->assign('sobrecargo', $sobrecargo_total);
        $this->context->smarty->assign('ordertotal', $cart->getordertotal() + $sobrecargo_total);
        
        return $this->display(__FILE__, "displayPickeoUpsAporvinoNotification.tpl");
    }
    
    public function hookDisplayPickeoUpsAporvino($params){       
        
        $cart = new Cart($_GET['id_cart']);
        
        $sql = 'SELECT * FROM '._DB_PREFIX_.'address WHERE id_address = '.(int)$cart->id_address_delivery.' and deleted = 0  ' ;
        $address =  Db::getInstance()->getRow($sql);

        $sql = 'SELECT * FROM ups_postal_code WHERE postal_code = '. $address['postcode'] ;
        //$sql = 'SELECT * FROM ups_postal_code WHERE postal_code = 18561' ;
        $reference_address =  Db::getInstance()->getRow($sql);
        
        // si le fuera a aplicar el recargo a todo el carrito
        // $peso = (float)$cart->getTotalWeight();
        $peso = 0;
        $sobrecargo_total = 0;
        
        // para calcular sobrecargo por productos de aporvino
        foreach($cart->getProducts() as $p){
            $product = new Product($p['id_product']);
            foreach ($product->getCarriers() as $c) {
                $name = strtolower($c['name']);
                if(strpos($name, 'aporvino') !== false){
                    $peso += (float)$product->weight;
                    break;
                }
            }
        }
        
        if($reference_address){
            
            
            if($reference_address['remote'] == 1){
                $tipo_area = 'r';
                if(Configuration::get("PICKEO_UPS_valor_minimo_remota") > $peso){
                    $sobrecargo_area = Configuration::get("PICKEO_UPS_sobrecargo_remota");
                    $sobrecargo_total = $peso * $sobrecargo_area;
                }
            }
            else {
                $tipo_area = 'nm';
                if(Configuration::get("PICKEO_UPS_valor_minimo_no_metropolitana") > $peso){
                    $sobrecargo_area = Configuration::get("PICKEO_UPS_sobrecargo_no_metropolitana");
                    $sobrecargo_total = $peso * $sobrecargo_area;
                }
            }
            
            $order = new Order($_GET['id_order']);
            $newPrice = (float)$order->total_products_wt + (float)$order->total_shipping + (float)$sobrecargo_total;
            $sql = 'UPDATE '. _DB_PREFIX_ .'orders' . ' SET total_paid = ' . $newPrice . ', total_paid_tax_incl = ' . $newPrice . ' WHERE id_order = ' . $_GET['id_order'];
            $result = Db::getInstance()->execute( $sql );
            
            if($sobrecargo_total >0){
                $this->context->smarty->assign("tipo_area",$tipo_area);
                $this->context->smarty->assign("postcode",$address['postcode']);
                $this->context->smarty->assign("peso",$peso);
                $this->context->smarty->assign("importe_peso",$sobrecargo_area);
                $this->context->smarty->assign("sobrecargo",$sobrecargo_total);
                $this->context->smarty->assign("importe_real",$newPrice);
                return $this->display(__FILE__, "displayPickeoUpsAporvino.tpl");
            }
        }
    }
    
