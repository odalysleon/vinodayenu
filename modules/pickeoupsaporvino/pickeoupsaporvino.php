    public function install() {
        
        if( !parent::install() )
            return false;
        
        // Variables del plugin
        Configuration::updateValue('PICKEO_UPS_valor_minimo_no_metropolitana', 21.70);
        Configuration::updateValue('PICKEO_UPS_sobrecargo_no_metropolitana', 0.44);
        Configuration::updateValue('PICKEO_UPS_valor_minimo_remota', 26.40);
        Configuration::updateValue('PICKEO_UPS_sobrecargo_remota', 0.56);
        
        // Instalar BD de zip codes
        $this->installDB();
        
        // Declaracion de hooks
        Hook::exec('displayPickeoUpsAporvino');
        $this->registerHook("displayPickeoUpsAporvino");
        Hook::exec('displayPickeoUpsAporvinoNotification');
        $this->registerHook("displayPickeoUpsAporvinoNotification");
        
        return true;
    }
    
    public function uninstall()    {
        if( !parent::uninstall() )
            return false;
        
        Configuration::deleteByName('PICKEO_UPS_valor_minimo_no_metropolitana');
        Configuration::deleteByName('PICKEO_UPS_sobrecargo_no_metropolitana');
        Configuration::deleteByName('PICKEO_UPS_valor_minimo_remota');
        Configuration::deleteByName('PICKEO_UPS_sobrecargo_remota');
        
        $this->uninstallDB();
        
        return true;
    }
    
    
    
